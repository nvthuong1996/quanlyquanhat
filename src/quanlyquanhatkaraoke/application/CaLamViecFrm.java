    public void displayTable() {
        try {
            CaLamViecDAO calamviecdao = DAOFactory.createCaLamViecDAO();
            listCa = calamviecdao.fillAll();
            String[] tblHead = {"Tên ca", "Giờ bắt đầu", "Giờ kết thúc"};
            DefaultTableModel model = new DefaultTableModel(tblHead, 0);
            for (CaLamViec ca : listCa) {
                List<String> list = new ArrayList<String>();
                list.add(ca.getTen());
                String a = ca.getGioBatDau() + "h";
                if (ca.getPhutBatDau() != 0) {
                    a += ca.getPhutBatDau() + "p";
                }
                list.add(a);
                String b = ca.getGioKetThuc() + "h";
                if (ca.getPhutKetThuc() != 0) {
                    b += ca.getPhutKetThuc() + "p";
                }
                list.add(b);
                model.addRow(list.toArray());
            }

            jButton2.setVisible(false);
            jButton3.setVisible(false);

            jTable1.setModel(model);

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tenca = jTextField2.getText();
        String batdau = jTextField1.getText();
        String kethuc = jTextField3.getText();
        String[] tmp = batdau.trim().split("h");
        CaLamViec calamviec = new CaLamViec();
        calamviec.setTen(tenca);
        if (tenca.equals("")) {
            failedMessage("Vui lòng nhập tên ca");
            return;
        }
        try {
            int h = Integer.parseInt(tmp[0]);
            int p;
            if (tmp.length == 1) {
                p = 0;
            } else {
                p = Integer.parseInt(tmp[1]);
            }
            calamviec.setGioBatDau(h);
            calamviec.setPhutBatDau(p);
        } catch (Exception ex) {
            failedMessage("Dữ liệu không đúng định dạng");
            return;
        }
        tmp = kethuc.trim().split("h");
        try {
            int h = Integer.parseInt(tmp[0]);
            int p;
            if (tmp.length == 1) {
                p = 0;
            } else {
                p = Integer.parseInt(tmp[1]);
            }
            calamviec.setGioKetThuc(h);
            calamviec.setPhutKetThuc(p);
        } catch (Exception ex) {
            failedMessage("Dữ liệu không đúng định dạng");
            return;
        }
        if (calamviec.getGioBatDau() > calamviec.getGioKetThuc()) {
            failedMessage("Dữ liệu không đúng định dạng");
            return;
        } else if (calamviec.getGioBatDau() == calamviec.getGioKetThuc() && calamviec.getPhutBatDau() == calamviec.getPhutKetThuc()) {
            failedMessage("Dữ liệu không đúng định dạng");
            return;
        }
        try {
            CaLamViecDAO calamviecdao = DAOFactory.createCaLamViecDAO();
            calamviecdao.insert(calamviec);
            displayTable();
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

